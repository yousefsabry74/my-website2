let mode = localStorage.getItem("mode");
let section = parseInt(localStorage.getItem("section") || "1");
let totalSections = 5;
let currentIndex = 0;

// الوقت الافتراضي: 25 دقيقة (1500 ثانية)
const DEFAULT_TIME_SECONDS = 25 * 60; 

let timeLeft = DEFAULT_TIME_SECONDS;

let questions = [];

// استرجاع الأسئلة والوقت من التخزين المؤقت
let savedQuestions = localStorage.getItem("questions");
if (savedQuestions) {
  questions = JSON.parse(savedQuestions);
  // تحميل الوقت المتبقي، وإذا لم يوجد نستخدم الافتراضي
  const savedTime = parseInt(localStorage.getItem("timeLeft") || DEFAULT_TIME_SECONDS);
  timeLeft = savedTime;
} else {
  // إنشاء قائمة الأسئلة بناءً على وضع الاختبار (Mode)
  
  if (mode === "quick") {
    // المحاكي السريع: أول 24 سؤالاً
    questions = fullQuestionsList.slice(0, 24).map(q => ({
      ...q,
      answer: null,
      marked: false,
    }));
  } else if (mode === "real") {
    // المحاكي الواقعي: تقسيم 120 سؤالاً على 5 أقسام (24 سؤال لكل قسم)
    const questionsPerSection = fullQuestionsList.length / totalSections;
    const startIndex = (section - 1) * questionsPerSection;
    const endIndex = startIndex + questionsPerSection;
    
    questions = fullQuestionsList.slice(startIndex, endIndex).map(q => ({
      ...q,
      answer: null,
      marked: false,
    }));
  } else {
    // حالة افتراضية
    questions = fullQuestionsList.slice(0, 5).map(q => ({
      ...q,
      answer: null,
      marked: false,
    }));
  }
}

function updateQuestion() {
  const q = questions[currentIndex];
  // تعديل العنوان ليشمل رقم السؤال من أصل العدد الكلي للقسم
  document.getElementById("section-title").textContent = (mode === 'real') ? 
    `القسم ${section} - السؤال ${currentIndex + 1} / ${questions.length}` :
    `السؤال ${currentIndex + 1} / ${questions.length}`;
    
  document.getElementById("question-text").textContent = q.text;

  let answersHTML = "";
  q.options.forEach((opt, i) => {
    answersHTML += `<label><input type="radio" name="answer" value="${i}" ${q.answer === i ? "checked" : ""}> ${opt}</label>`;
  });
  document.getElementById("answers").innerHTML = answersHTML;

  // زر إنهاء الامتحان يظهر فقط في آخر سؤال في آخر قسم (أو في السريع)
  const endExamBtn = document.getElementById("end-exam");
  if (currentIndex === questions.length - 1 && (mode === "quick" || section === totalSections)) {
    endExamBtn.style.display = "block";
  } else {
    endExamBtn.style.display = "none";
  }
}

function saveAnswer() {
  const selected = document.querySelector("input[name='answer']:checked");
  questions[currentIndex].answer = selected ? parseInt(selected.value) : null;
  // حفظ الأسئلة والوقت في كل مرة يتم فيها الحفظ
  localStorage.setItem("questions", JSON.stringify(questions));
  localStorage.setItem("timeLeft", timeLeft);
}

function nextQuestion() {
  saveAnswer();
  if (currentIndex < questions.length - 1) {
    currentIndex++;
    updateQuestion();
  }
}

function prevQuestion() {
  saveAnswer();
  if (currentIndex > 0) {
    currentIndex--;
    updateQuestion();
  }
}

function markQuestion() {
  questions[currentIndex].marked = true;
  saveAnswer();
  alert("⭐ تم وضع علامة مرجعية");
}

function reviewSection() {
  saveAnswer();
  let html = `<h2>مراجعة القسم ${section}</h2><ul>`;
  questions.forEach((q, i) => {
    let status = q.answer !== null ? "✅ مجاب" : "❌ غير مجاب";
    if (q.marked) status += " ⭐ مرجعي";
    html += `<li>سؤال ${i + 1}: ${status} <button onclick="goTo(${i})">🔁</button></li>`;
  });
  html += `</ul>
    <button onclick="goTo(0)">🔙 العودة لأول سؤال</button>
    <button onclick="chooseQuestion()">🔍 العودة لسؤال محدد</button>
    <button onclick="endSection()">✅ إنهاء القسم</button>`;
  document.body.innerHTML = html;
}

function goTo(index) {
  saveAnswer();
  localStorage.setItem("returnTo", index);
  // نحفظ الأسئلة هنا بالفعل في saveAnswer
  location.reload();
}

